// Returns an array of text angles and values for a given group and step.
function nodeNumbers (d) {
  const start = d.startAngle / 2;
  const end = d.endAngle / 2;
  const item = parseInt(d.index, 10) + 1;
  const k = (end - start) / item;
  return [{ value: item, angle: item * k + d.startAngle }];
}

const chord = ({ size = 500 }) => {
  const width = size;
  const height = size;
  const outerRadius = size * 0.5;
  const innerRadius = outerRadius - 50;

  // SVG
  const svg = d3.select('body').append('svg');
  svg.attr('width', width)
    .attr('height', height);

  // Chord
  const chordPadRad = 0.01;
  const chord = d3.chord()
    .padAngle(chordPadRad)
    .sortSubgroups(d3.descending);

  let chords = chord(matrix);

  // Process a chord to have equal arc size & distribution, via startAngle/endAngle
  const processChordAngle = (options, chord) => {
    const startAngle = chord.index * (options.nodeRad + chordPadRad);
    const endAngle = startAngle + options.nodeRad;
    return { ...chord, startAngle, endAngle };
  };

  // Process a chord's array-like object to have equal arc size & distribution
  const processChords = (chords) => {
    const options = {
      nodes: chords['groups'].length,
      totalRads: 360 / (180 / Math.PI),
    };
    options.padRads = options.nodes * chordPadRad;
    options.nodeRad = (options.totalRads - options.padRads) / options.nodes;

    _.each(chords, (chord, key) => {
      chords[key].source = processChordAngle(options, chord.source);
      chords[key].target = processChordAngle(options, chord.target);
    });

    // Handle groups
    chords.groups = _.map(chords.groups, (chord) => processChordAngle(options, chord));

    return chords;
  };

  chords = processChords(chords);

  // Arc
  const arc = d3.arc()
    .innerRadius(innerRadius)
    .outerRadius(outerRadius);

  // Ribbon
  const ribbon = d3.ribbon()
    //.startAngle(startAngle)
    .radius(innerRadius - 5);

  // Colours
  const color = d3.scaleOrdinal()
    .domain(d3.range(1))
    .range(["#4c5e6c"]);

  // Group data
  let g = svg.append("g")
    .attr("transform", `translate(${width / 2}, ${height / 2})`)
    .datum(chords);

  let group = g.append("g")
    .attr("class", "groups")
    .selectAll("g")
    .data(chords => chords.groups)
    .enter().append("g");

  group.append("path")
    .style("fill", d => color(d.index))
    .style("stroke", d => d3.rgb(color(d.index)).darker())
    .attr("d", arc);

  let groupText = group.selectAll(".group-text")
    .attr("class", "group-text")
    .data(nodeNumbers)
    .enter().append("g")
    .attr("transform", d => `rotate(${(d.angle) * 180 / Math.PI - 90}) translate(${innerRadius + 25},0)`);

  groupText.append('text')
    .attr("x", 0)
    .attr("y", 0)
    .attr("dy", ".35em")
    .attr("transform", d => `rotate(${90 - (d.angle * 180 / Math.PI)})`)
    .style("text-anchor", "middle")
    .text(d => _.padStart(d.value, 2, 0));

  g.append("g")
    .attr("class", "ribbons")
    .selectAll("path")
    .data(chords => chords)
    .enter().append("path")
    .attr("d", ribbon)
    .style("fill", d => color(d.target.index))
    .style("stroke", d => d3.rgb(color(d.target.index)).darker());
};

export default chord;
